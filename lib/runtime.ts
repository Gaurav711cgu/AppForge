// ── EXECUTION VALIDATOR: Check if generated code is syntactically valid ──
export function validateGeneratedCode(files: Record<string, string>): {
  valid: boolean;
  errors: string[];
} {
  const errors: string[] = [];

  for (const [path, content] of Object.entries(files)) {
    if (path.endsWith(".ts") || path.endsWith(".tsx")) {
      // Basic syntax checks
      const openBraces = (content.match(/\{/g) ?? []).length;
      const closeBraces = (content.match(/\}/g) ?? []).length;
      if (Math.abs(openBraces - closeBraces) > 2) {
        errors.push(`${path}: Unbalanced braces (${openBraces} open, ${closeBraces} close)`);
      }

      if (content.includes("undefined.") || content.includes("null.")) {
        errors.push(`${path}: Potential null reference`);
      }
    }

    if (path.endsWith(".sql")) {
      if (!content.includes("CREATE TABLE") && !content.includes("create table")) {
        errors.push(`${path}: SQL migration missing CREATE TABLE statements`);
      }
    }
  }

  return { valid: errors.length === 0, errors };
}

// ── HELPERS ──────────────────────────────────────────────────────
function toPascalCase(str: string): string {
  return str.replace(/(^|_)(\w)/g, (_, __, c) => c.toUpperCase());
}

function sqlToTsType(sqlType: string): string {
  const lower = sqlType.toLowerCase();
  if (lower.includes("int") || lower.includes("float") || lower.includes("decimal") || lower.includes("numeric")) return "number";
  if (lower.includes("bool")) return "boolean";
  if (lower.includes("timestamp") || lower.includes("date")) return "string";
  if (lower.includes("json")) return "Record<string, unknown>";
  if (lower.includes("uuid")) return "string";
  return "string";
}

function endpointToFunctionName(method: string, path: string): string {
  const parts = path.replace(/\//g, "_").replace(/:/g, "").replace(/-/g, "_").split("_").filter(Boolean);
  const action = method.toLowerCase();
  return `${action}${parts.map(p => p.charAt(0).toUpperCase() + p.slice(1)).join("")}`;
}

function extractPathParams(path: string): string[] {
  return [...path.matchAll(/:([a-z_]+)/g)].map(m => m[1]);
}
