import type { AppConfig, UISchema } from "@/types";

// ── CODE GENERATOR: Produces actual React/Next.js files ──────────
export function generateRuntimeCode(config: AppConfig): Record<string, string> {
  const files: Record<string, string> = {};

  // 1. Generate types from DB schema
  files["types/generated.ts"] = generateTypes(config);

  // 2. Generate API client from API schema
  files["lib/api-client.ts"] = generateAPIClient(config);

  // 3. Generate database migrations
  files["db/migrations/001_init.sql"] = config.db.migration_sql;

  // 4. Generate auth config
  files["lib/auth.ts"] = generateAuthConfig(config);

  // 5. Generate each page
  for (const page of config.ui.pages) {
    const filePath = `app/${page.path === "/" ? "page" : page.path.replace(/^\//, "").replace(/\//g, "/")}/page.tsx`;
    files[filePath] = generatePage(page, config);
  }

  // 6. Generate layout
  files["app/layout.tsx"] = generateLayout(config);

  // 7. Generate environment template
  files[".env.example"] = generateEnvTemplate(config);

  // 8. Generate package.json
  files["package.json"] = generatePackageJson(config);

  return files;
}

function generateTypes(config: AppConfig): string {
  const types = config.db.tables.map(table => {
    const typeName = toPascalCase(table.name);
    const fields = table.columns.map(col => {
      const tsType = sqlToTsType(col.sql_type);
      return `  ${col.name}${col.nullable ? "?" : ""}: ${tsType};`;
    }).join("\n");
    return `export interface ${typeName} {\n${fields}\n}`;
  });

  return `// AUTO-GENERATED BY APPFORGE — DO NOT EDIT MANUALLY\n// Generated at: ${config.meta.generated_at}\n\n${types.join("\n\n")}`;
}

function generateAPIClient(config: AppConfig): string {
  const methods = config.api.endpoints.map(endpoint => {
    const fnName = endpointToFunctionName(endpoint.method, endpoint.path);
    const hasBody = ["POST", "PUT", "PATCH"].includes(endpoint.method);
    const params = [
      ...extractPathParams(endpoint.path).map(p => `${p}: string`),
      ...(hasBody ? ["body: Record<string, unknown>"] : []),
      ...(endpoint.query_params?.length ? ["params?: Record<string, string>"] : []),
    ].join(", ");

    const pathWithParams = endpoint.path.replace(/:([a-z_]+)/g, "${$1}");
    const queryString = endpoint.query_params?.length ? "`?${new URLSearchParams(params)}`" : '""';

    return `
export async function ${fnName}(${params}) {
  const url = \`${config.api.base_path}${pathWithParams}\`${endpoint.query_params?.length ? " + " + queryString : ""};
  const res = await fetch(url, {
    method: "${endpoint.method}",
    ${endpoint.auth_required ? 'headers: { "Authorization": \`Bearer \${getToken()}\`, "Content-Type": "application/json" },' : 'headers: { "Content-Type": "application/json" },'}
    ${hasBody ? "body: JSON.stringify(body)," : ""}
  });
  if (!res.ok) throw new Error(await res.text());
  return res.json();
}`;
  });

  return `// AUTO-GENERATED API CLIENT — AppForge Runtime\n\nfunction getToken(): string {
  return typeof window !== "undefined" ? localStorage.getItem("access_token") ?? "" : "";
}\n${methods.join("\n")}`;
}

function generatePage(page: UISchema["pages"][0], config: AppConfig): string {
  const imports = new Set(["\"use client\"", 'import { useState, useEffect } from "react"']);

  const componentCode = page.components.map(component => {
    switch (component.type) {
      case "DataTable":
        return generateDataTable(component);
      case "Form":
        return generateForm(component);
      case "StatCard":
        return generateStatCard(component);
      case "Chart":
        return generateChart(component);
      default:
        return `<div className="p-4 border rounded">${component.title ?? component.type}</div>`;
    }
  }).join("\n        ");

  return `"use client";
import { useState, useEffect } from "react";

// AUTO-GENERATED PAGE: ${page.name}
// Path: ${page.path}
// Auth required: ${page.auth_required}
// Roles: ${page.roles.join(", ") || "all"}

export default function ${toPascalCase(page.name.replace(/\s/g, ""))}Page() {
  const [data, setData] = useState<Record<string, unknown>[]>([]);
  const [loading, setLoading] = useState(false);
  const [error, setError] = useState<string | null>(null);

  return (
    <div className="p-6 space-y-6">
      <h1 className="text-2xl font-semibold">${page.name}</h1>
      ${componentCode}
    </div>
  );
}`;
}

function generateDataTable(component: UISchema["pages"][0]["components"][0]): string {
  return `
      {/* DataTable: ${component.title ?? "Data"} */}
      <div className="border rounded-lg overflow-hidden">
        <div className="p-4 border-b flex items-center justify-between">
          <h2 className="font-medium">${component.title ?? "Records"}</h2>
          ${component.actions?.map(a => `<button className="px-3 py-1 text-sm bg-blue-600 text-white rounded" onClick={() => {}}>${a.label}</button>`).join("") ?? ""}
        </div>
        <table className="w-full text-sm">
          <thead className="bg-gray-50">
            <tr>
              ${component.fields?.map(f => `<th className="px-4 py-2 text-left text-xs font-medium text-gray-500 uppercase">${f.label}</th>`).join("") ?? "<th>ID</th><th>Name</th>"}
            </tr>
          </thead>
          <tbody className="divide-y">
            {data.map((row, i) => (
              <tr key={i} className="hover:bg-gray-50">
                ${component.fields?.map(f => `<td className="px-4 py-2">{String(row['${f.name}'] ?? '')}</td>`).join("") ?? "<td>...</td>"}
              </tr>
            ))}
          </tbody>
        </table>
        {loading && <div className="p-4 text-center text-gray-400">Loading...</div>}
        {!loading && data.length === 0 && <div className="p-4 text-center text-gray-400">No data</div>}
      </div>`;
}

function generateForm(component: UISchema["pages"][0]["components"][0]): string {
  const fields = component.fields?.map(f => {
    if (f.type === "select") {
      return `
            <div>
              <label className="block text-sm font-medium text-gray-700 mb-1">${f.label}${f.required ? " *" : ""}</label>
              <select name="${f.name}" className="w-full border rounded px-3 py-2 text-sm" ${f.required ? "required" : ""}>
                ${f.options?.map(o => `<option value="${o}">${o}</option>`).join("") ?? ""}
              </select>
            </div>`;
    }
    return `
            <div>
              <label className="block text-sm font-medium text-gray-700 mb-1">${f.label}${f.required ? " *" : ""}</label>
              <input type="${f.type}" name="${f.name}" placeholder="${f.label}" className="w-full border rounded px-3 py-2 text-sm" ${f.required ? "required" : ""} />
            </div>`;
  }).join("") ?? "";

  return `
      {/* Form: ${component.title ?? "Form"} */}
      <div className="border rounded-lg p-6 max-w-lg">
        <h2 className="font-medium mb-4">${component.title ?? "Form"}</h2>
        <form className="space-y-4" onSubmit={(e) => { e.preventDefault(); }}>
          ${fields}
          <button type="submit" className="w-full bg-blue-600 text-white py-2 rounded text-sm font-medium">Submit</button>
        </form>
      </div>`;
}

