function generateAuthConfig(config: AppConfig): string {
  return `// AUTO-GENERATED AUTH CONFIG — AppForge Runtime
// Strategy: ${config.auth.strategy}
// Roles: ${config.auth.roles.map(r => r.name).join(", ")}

export const AUTH_CONFIG = ${JSON.stringify(config.auth, null, 2)};

export function hasPermission(role: string, resource: string, action: string): boolean {
  const roleConfig = AUTH_CONFIG.roles.find(r => r.name === role);
  if (!roleConfig) return false;
  const permission = roleConfig.permissions.find(p => p.resource === resource);
  if (!permission) return false;
  return permission.actions.includes(action as never);
}`;
}

